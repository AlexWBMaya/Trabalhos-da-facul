    private void BotaoCalcularSalarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoCalcularSalarioActionPerformed
        // TODO add your handling code here:
        try{
        int matricula = Integer.parseInt(textFieldMatricula.getText());
        String nomeCompleto = textFieldNomeCompleto.getText();
        int dependentes = Integer.parseInt(textFieldNumeroDeDependentes.getText());
        int producao = Integer.parseInt(textFieldProducao.getText());
        float salarioBase = Float.parseFloat(textfieldSalarioBase.getText());
            
        classe.ClasseSalario funcionario1 = new ClasseSalario(matricula, nomeCompleto, dependentes, producao, salarioBase);
        funcionario1.gratificacao();
        funcionario1.salarioBruto();
        funcionario1.descontoDoINSS();
        funcionario1.descontoDoIRPF();
        funcionario1.descontoDependentes();
        funcionario1.descontos();
               
        jTextArea1.setText(funcionario1.saida());
        }catch (Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_BotaoCalcularSalarioActionPerformed


